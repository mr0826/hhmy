package cosDataPack;

public class Av extends Shohin{

	/** 色 */
	private String color;
	/** 型番 */
	private String kataban;
	/** 画面サイズ */
	private String size;
	/** 画面解像度 */
	private String resolution;
	/** 出力音量 */
	private String sound;
	/** 記憶容量 */
	private String capacity;
	/** 対応メディア */
	private String media;
	/** インターフェース */
	private String face;
	/** 消費電力 */
	private String C_power;
	/** 付属品 */
	private String accessory;
	/** 保証期間 */
	private String A_period;
	/** 質量 */
	private String mass;
	/** 寸法 */
	private String measure;

	/**
	 * 引数なしコンストラクタ。
	 */
	 public Av() {
		super();
	}

	/**
	 * 色を取得する
	 * @return 色
	 */
	public String getColor() {
		return color;
	}

	/**
	 * 色を設定する
	 * @param color 色
	 */
	public void setColor(String color) {
		this.color = color;
	}

	/**
	 * 型番を取得する
	 * @return 型番
	 */
	public String getKataban() {
		return kataban;
	}

	/**
	 * 型番を設定する
	 * @param kataban 型番
	 */
	public void setKataban(String kataban) {
		this.kataban = kataban;
	}

	/**
	 * サイズを取得する
	 * @return サイズ
	 */
	public String getSize() {
		return size;
	}

	/**
	 * サイズを設定する
	 * @param size サイズ
	 */
	public void setSize(String size) {
		this.size = size;
	}

	/**
	 * 解像度を取得する
	 * @return 解像度
	 */
	public String getResolution() {
		return resolution;
	}

	/**
	 * 解像度を設定する
	 * @param resolution 解像度
	 */
	public void setResolution(String resolution) {
		this.resolution = resolution;
	}

	/**
	 * 音量を取得する
	 * @return 音量
	 */
	public String getSound() {
		return sound;
	}

	/**
	 * 音量を設定する
	 * @param sound 音量
	 */
	public void setSound(String sound) {
		this.sound = sound;
	}

	/**
	 * 容量を取得する
	 * @return 容量
	 */
	public String getCapacity() {
		return capacity;
	}

	/**
	 * 容量を設定する
	 * @param capacity 容量
	 */
	public void setCapacity(String capacity) {
		this.capacity = capacity;
	}

	/**
	 * メディアを取得する
	 * @return メディア
	 */
	public String getMedia() {
		return media;
	}

	/**
	 * メディアを設定する
	 * @param media メディア
	 */
	public void setMedia(String media) {
		this.media = media;
	}

	/**
	 * インターフェースを取得する
	 * @return インターフェース
	 */
	public String getFace() {
		return face;
	}

	/**
	 * インターフェースを設定する
	 * @param face インターフェース
	 */
	public void setFace(String face) {
		this.face = face;
	}

	/**
	 * 電力を取得する
	 * @return 電力
	 */
	public String getC_Power() {
		return C_power;
	}

	/**
	 * 電力を設定する
	 * @param C_power 電力
	 */
	public void setC_Power(String C_power) {
		this.C_power = C_power;
	}

	/**
	 * 付属品を取得する
	 * @return 付属品
	 */
	public String getAccessory() {
		return accessory;
	}

	/**
	 * 付属品を設定する
	 * @param accessory 付属品
	 */
	public void setAccessory(String accessory) {
		this.accessory = accessory;
	}

	/**
	 * 保証期間を取得する
	 * @return 保証期間
	 */
	public String getA_period() {
		return A_period;
	}

	/**
	 * 保証期間を設定する
	 * @param A_period 保証期間
	 */
	public void setA_period(String A_period) {
		this.A_period = A_period;
	}

	/**
	 * 質量を取得する
	 * @return 質量
	 */
	public String getMass() {
		return mass;
	}

	/**
	 * 質量を設定する
	 * @param mass 質量
	 */
	public void setMass(String mass) {
		this.mass = mass;
	}

	/**
	 * 寸法を取得する
	 * @return 寸法
	 */
	public String getMeasure() {
		return measure;
	}

	/**
	 * 寸法を設定する
	 * @param measure 寸法
	 */
	public void setMeasure(String measure) {
		this.measure = measure;
	}



}
